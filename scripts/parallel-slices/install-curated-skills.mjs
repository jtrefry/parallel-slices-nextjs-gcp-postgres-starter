#!/usr/bin/env node

import { spawnSync } from "node:child_process";
import { createHash } from "node:crypto";
import {
  cpSync,
  existsSync,
  lstatSync,
  mkdirSync,
  mkdtempSync,
  readFileSync,
  readdirSync,
  realpathSync,
  renameSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { devNull, tmpdir } from "node:os";
import {
  basename,
  dirname,
  isAbsolute,
  join,
  relative,
  resolve,
  sep,
} from "node:path";
import { fileURLToPath } from "node:url";

import {
  readAgentProfile,
  skillDirectoryForAgent,
  validateAgent,
} from "./agent-profile.mjs";

const markerName = ".parallel-slices-upstream.json";
const inventoryMarker =
  "<!-- generated by install-curated-skills.mjs; do not edit -->";
const allowedExtensions = new Set([".json", ".md"]);
const maximumFileBytes = 512_000;
const maximumSkillBytes = 2_000_000;

function fail(message) {
  throw new Error(message);
}

function git(args, cwd) {
  const result = spawnSync("git", args, {
    cwd,
    encoding: "utf8",
    env: {
      ...process.env,
      GIT_CONFIG_GLOBAL: devNull,
      GIT_CONFIG_NOSYSTEM: "1",
      GIT_TERMINAL_PROMPT: "0",
    },
  });
  if (result.error) fail(`git could not start: ${result.error.message}`);
  if (result.status !== 0) {
    fail(`git ${args[0]} failed: ${(result.stderr || "").trim()}`);
  }
  return result.stdout.trim();
}

function isSafeRelativePath(path, prefix) {
  return (
    typeof path === "string" &&
    path.startsWith(prefix) &&
    !path.startsWith("/") &&
    !path.split("/").some((part) => !part || part === "." || part === "..")
  );
}

export function validateCuratedSkillsManifest(manifest) {
  if (!manifest || manifest.schema !== 1) fail("unsupported skills manifest");
  const source = manifest.source;
  if (
    !source ||
    typeof source.repository !== "string" ||
    !/^(https:\/\/github\.com\/[A-Za-z0-9_.-]+\/[A-Za-z0-9_.-]+\.git|file:\/\/)/.test(
      source.repository,
    )
  ) {
    fail("skills repository must be an HTTPS GitHub or file URL");
  }
  if (!/^[0-9a-f]{40}$/.test(source.commit || "")) {
    fail("skills source must use a full Git commit SHA");
  }
  if (!/^\d{4}-\d{2}-\d{2}$/.test(source.reviewedAt || "")) {
    fail("skills source must include a reviewedAt date");
  }
  if (!Array.isArray(manifest.skills) || manifest.skills.length === 0) {
    fail("skills manifest must select at least one skill");
  }
  const sources = new Set();
  const names = new Set();
  for (const skill of manifest.skills) {
    if (!isSafeRelativePath(skill.source, "skills/")) {
      fail(`unsafe skill source path: ${skill.source}`);
    }
    if (!/^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(skill.name || "")) {
      fail(`invalid skill name: ${skill.name}`);
    }
    if (skill.license !== "MIT")
      fail(`unsupported skill license: ${skill.license}`);
    if (!/^[0-9a-f]{64}$/.test(skill.treeSha256 || "")) {
      fail(`invalid tree hash for ${skill.name}`);
    }
    if (sources.has(skill.source) || names.has(skill.name)) {
      fail(`duplicate curated skill path: ${skill.name}`);
    }
    sources.add(skill.source);
    names.add(skill.name);
  }
  return manifest;
}

function walkFiles(directory, options = {}) {
  const files = [];
  for (const entry of readdirSync(directory, { withFileTypes: true })) {
    if (options.ignoreMarker && entry.name === markerName) continue;
    const absolute = join(directory, entry.name);
    const metadata = lstatSync(absolute);
    if (metadata.isSymbolicLink())
      fail(`skill contains a symlink: ${absolute}`);
    if (metadata.isDirectory()) files.push(...walkFiles(absolute, options));
    else if (metadata.isFile()) files.push(absolute);
    else fail(`skill contains an unsupported entry: ${absolute}`);
  }
  return files.sort();
}

export function hashSkillTree(directory, options = {}) {
  const hash = createHash("sha256");
  for (const file of walkFiles(directory, options)) {
    hash.update(relative(directory, file).split(sep).join("/"));
    hash.update("\0");
    hash.update(readFileSync(file));
    hash.update("\0");
  }
  return hash.digest("hex");
}

function validateSkillTree(directory, skill) {
  const files = walkFiles(directory);
  let totalBytes = 0;
  for (const file of files) {
    const extension = file.slice(file.lastIndexOf("."));
    if (!allowedExtensions.has(extension)) {
      fail(`curated skill contains executable or unsupported content: ${file}`);
    }
    const bytes = lstatSync(file).size;
    if (bytes > maximumFileBytes)
      fail(`curated skill file is too large: ${file}`);
    totalBytes += bytes;
  }
  if (totalBytes > maximumSkillBytes)
    fail(`curated skill is too large: ${skill.name}`);
  const skillFile = join(directory, "SKILL.md");
  if (!existsSync(skillFile))
    fail(`curated skill is missing SKILL.md: ${skill.name}`);
  const frontmatter = readFileSync(skillFile, "utf8").split("---", 3)[1] || "";
  if (
    !new RegExp(`^name:\\s*["']?${skill.name}["']?\\s*$`, "m").test(frontmatter)
  ) {
    fail(`curated skill name does not match its manifest: ${skill.name}`);
  }
  if (!/^license:\s*["']?MIT["']?\s*$/m.test(frontmatter)) {
    fail(
      `curated skill does not declare the expected MIT license: ${skill.name}`,
    );
  }
  const actualHash = hashSkillTree(directory);
  if (actualHash !== skill.treeSha256) {
    fail(`curated skill hash mismatch: ${skill.name}`);
  }
}

function assertNoTargetSymlink(root, path) {
  let current = root;
  for (const segment of path.split("/")) {
    current = join(current, segment);
    if (existsSync(current) && lstatSync(current).isSymbolicLink()) {
      fail(`refusing to write through a target symlink: ${current}`);
    }
  }
}

function readManagedMarker(target) {
  const marker = join(target, markerName);
  if (!existsSync(marker))
    fail(`refusing to replace an unmanaged skill: ${target}`);
  if (lstatSync(marker).isSymbolicLink())
    fail(`refusing symlinked skill metadata: ${marker}`);
  try {
    return JSON.parse(readFileSync(marker, "utf8"));
  } catch (error) {
    fail(`invalid curated skill metadata in ${marker}: ${error.message}`);
  }
}

function installSkill(source, targetRoot, manifest, skill, targetPath, agent) {
  assertNoTargetSymlink(targetRoot, targetPath);
  const target = resolve(targetRoot, targetPath);
  const parent = dirname(target);
  mkdirSync(parent, { recursive: true });
  if (existsSync(target)) {
    if (!lstatSync(target).isDirectory())
      fail(`skill target is not a directory: ${target}`);
    const marker = readManagedMarker(target);
    const actualHash = hashSkillTree(target, { ignoreMarker: true });
    if (actualHash !== marker.treeSha256) {
      fail(`refusing to overwrite a modified curated skill: ${skill.name}`);
    }
    if (
      actualHash === skill.treeSha256 &&
      marker.commit === manifest.source.commit
    ) {
      console.log(`current: ${targetPath}`);
      return;
    }
  }

  const incoming = join(parent, `.${basename(target)}.incoming-${process.pid}`);
  const backup = join(parent, `.${basename(target)}.backup-${process.pid}`);
  if (existsSync(incoming) || existsSync(backup)) {
    fail(`stale curated skill staging path exists beside ${target}`);
  }
  cpSync(source, incoming, { recursive: true, errorOnExist: true });
  writeFileSync(
    join(incoming, markerName),
    `${JSON.stringify(
      {
        repository: manifest.source.repository,
        commit: manifest.source.commit,
        source: skill.source,
        name: skill.name,
        agent,
        license: skill.license,
        treeSha256: skill.treeSha256,
      },
      null,
      2,
    )}\n`,
  );

  const hadExisting = existsSync(target);
  try {
    if (hadExisting) renameSync(target, backup);
    renameSync(incoming, target);
    if (hadExisting) rmSync(backup, { recursive: true, force: true });
  } catch (error) {
    if (!existsSync(target) && existsSync(backup)) renameSync(backup, target);
    rmSync(incoming, { recursive: true, force: true });
    throw error;
  }
  console.log(`installed: ${targetPath}`);
}

function renderInventory(manifest, agent) {
  const skills = manifest.skills
    .map(
      (skill) =>
        `- \`${skill.name}\`: [source](https://github.com/vercel-labs/agent-skills/tree/${manifest.source.commit}/${skill.source}), ${skill.license}`,
    )
    .join("\n");
  return `${inventoryMarker}
# Curated third-party agent skills

These unmodified skills were selected by the installed architecture package and are
pinned to Vercel's \`${manifest.source.commit}\` commit, reviewed on
${manifest.source.reviewedAt}. Project requirements, security policy, and
the selected architecture take precedence over advisory skill guidance.
This inventory is the native copy for the enabled \`${agent}\` controller.

${skills}

The upstream repository and individual skill metadata declare the skills under
the MIT license. Do not use a bulk installer here: the upstream collection also
contains deployment and platform-specific skills that are outside this
project's recorded architecture and authorization boundary.
`;
}

function writeInventory(targetRoot, manifest, skillDirectory, agent) {
  const inventoryPath = `${skillDirectory}/THIRD_PARTY.md`;
  assertNoTargetSymlink(targetRoot, inventoryPath);
  const target = resolve(targetRoot, inventoryPath);
  if (existsSync(target)) {
    const existing = readFileSync(target, "utf8");
    if (!existing.startsWith(inventoryMarker)) {
      fail(
        `refusing to overwrite an unmanaged third-party inventory: ${target}`,
      );
    }
  }
  const incoming = join(
    dirname(target),
    `.THIRD_PARTY.md.incoming-${process.pid}`,
  );
  if (existsSync(incoming))
    fail(`stale inventory staging path exists: ${incoming}`);
  writeFileSync(incoming, renderInventory(manifest, agent));
  renameSync(incoming, target);
}

export function installCuratedSkills({ target, manifestPath, agent }) {
  const targetRoot = resolve(target);
  if (!existsSync(targetRoot) || !lstatSync(targetRoot).isDirectory()) {
    fail(`target repository does not exist: ${targetRoot}`);
  }
  const resolvedManifest =
    manifestPath || resolve(targetRoot, ".parallel-slices/curated-skills.json");
  const manifest = validateCuratedSkillsManifest(
    JSON.parse(readFileSync(resolvedManifest, "utf8")),
  );
  const targetAgents = agent
    ? [validateAgent(agent)]
    : readAgentProfile(targetRoot).enabledControllers;
  const checkout = mkdtempSync(join(tmpdir(), "parallel-slices-skills-"));
  try {
    git(["init", "--quiet"], checkout);
    git(["remote", "add", "origin", manifest.source.repository], checkout);
    git(
      ["fetch", "--quiet", "--depth=1", "origin", manifest.source.commit],
      checkout,
    );
    const fetched = git(["rev-parse", "FETCH_HEAD"], checkout);
    if (fetched !== manifest.source.commit)
      fail("fetched skills commit does not match manifest");
    git(
      [
        "checkout",
        "--quiet",
        "FETCH_HEAD",
        "--",
        ...manifest.skills.map((skill) => skill.source),
      ],
      checkout,
    );
    const selected = manifest.skills.map((skill) => ({
      skill,
      source: resolve(checkout, skill.source),
    }));
    for (const { skill, source } of selected) validateSkillTree(source, skill);
    for (const targetAgent of targetAgents) {
      const skillDirectory = skillDirectoryForAgent(targetAgent);
      for (const { skill, source } of selected) {
        const targetPath = `${skillDirectory}/${skill.name}`;
        installSkill(
          source,
          targetRoot,
          manifest,
          skill,
          targetPath,
          targetAgent,
        );
      }
      writeInventory(targetRoot, manifest, skillDirectory, targetAgent);
      console.log(
        `curated skills verified for ${targetAgent} at ${manifest.source.commit}`,
      );
    }
  } finally {
    rmSync(checkout, { recursive: true, force: true });
  }
}

const isMain =
  process.argv[1] &&
  fileURLToPath(import.meta.url) === realpathSync(resolve(process.argv[1]));
if (isMain) {
  try {
    if (process.argv.length > 3)
      fail("usage: install-curated-skills.mjs [absolute-target]");
    const target = process.argv[2] || process.cwd();
    if (!isAbsolute(target)) fail("target must be an absolute path");
    installCuratedSkills({ target });
  } catch (error) {
    console.error(`CURATED SKILLS FAILED: ${error.message}`);
    process.exitCode = 1;
  }
}
